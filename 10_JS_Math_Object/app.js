//sqrt
let squareroot = Math.sqrt(144);
console.log(`The Squareroot of 144 is : ${squareroot}`);

//pow
let power = Math.pow(2, 6);
console.log(power);

//min
// let numbers = [12, 43, 56, 64, 34, 28, 18, 86, 40, 45];
let minimum = Math.min(12, 43, 56, 64, 34, 28, 18, 86, 40, 45);
console.log(minimum);

//max
let maximum = Math.max(12, 43, 56, 64, 34, 28, 18, 86, 40, 45);
console.log(maximum);

//random number -- to get a random number
let randomNumber = Math.random();
console.log(randomNumber);

//to get answer to 4 decimal places
randomNumber = Math.random() * 10000;
console.log(randomNumber);

//to make it a round number
randomNumber = Math.round(Math.random() * 10000);
console.log(randomNumber);

